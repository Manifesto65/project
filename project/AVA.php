<p>Common Modular Avionics System for Nano-Launchers Offering Affordable Access to Space.<p>
<br>
<p>Small satellites are becoming ever more capable of performing valuable missions for both government and commercial customers.  However, currently these satellites can only be launched affordably are secondary payloads.  This makes it difficult for the small satellite mission to launch when needed, to the desired orbit, and with acceptable risk.</p>
<br>
<p>What is needed is a class of low-cost launchers, so that launch costs to low Earth orbit (LEO) are commensurate with payload costs.
</p><br>
<p>
Several private and government-sponsored launch vehicle developers are working toward just that - the ability to affordably insert small payloads into LEO.  But until now, the cost of the complex avionics has still remained disproportionately high. AVE solves this problem.</p>

<p>Significant contributors to the cost of launching nano-satellites to orbit are the avionics and software systems that steer and control the launch vehicles, sequence stage separation, deploy payloads, and telemeter data. &nbsp;The high costs of these Guidance, Navigation and Control (GNC) avionics systems are due in part to the current practice of developing unique, single-use hardware and software for each launch. &nbsp;High-performance, high-reliability inertial sensor components with heritage from legacy launchers also contribute to costs - but can low-cost commercial inertial sensors work just as well?</p>
<p>NASA Ames Research Center has developed and tested a prototype low-cost avionics package for space launch vehicles that provides complete GNC functionality in a package smaller than a tissue box (100mm x 120mm x 69mm; 4in x 4.7in x 2.7in), with a mass of less than 0.84kg (2lbs). &nbsp;AVA takes advantage of commercially available, low-cost, mass-produced, miniaturized sensors, filtering their more noisy inertial data with real-time GPS data. &nbsp;The goal of the AVA project is to produce and flight-verify a common suite of avionics and software that deliver affordable, capable GNC and telemetry avionics with application to multiple nano-launch vehicles at 1% of the cost of current state-of-the-art avionics.</p>
<p>
		
</p><p>
	
</p>
<p>
	
</p>
<p>In the test lab, the AVA has been successfully demonstrated to survive the launch environment. &nbsp;Simulations using digital models of rockets guided by AVA demonstrate that it achieves all functional requirements under a variety of simulated launch conditions.</p>

